package bean;

public class ren1_2bean {
	private int gakuseki;
	private String name;
	public ren1_2bean(int gakuseki, String name) {
		super();
		this.gakuseki = gakuseki;
		this.name = name;
	}
	public int getGakuseki() {
		return gakuseki;
	}
	public void setGakuseki(int gakuseki) {
		this.gakuseki = gakuseki;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}

